import SwiftUI
import Firebase
import FirebaseFirestore

struct TextSearchView: View {
    @State private var searchText: String = ""
    @State private var recentMeds: [String] = []
    @State private var popularMeds: [String] = []
    let userId = "user1"

    var body: some View {
        NavigationView {
            ScrollView {
                VStack(alignment: .leading, spacing: 20) {

                    // MARK: ÌÉêÏÉâÏ∞Ω
                    HStack {
                        Image(systemName: "magnifyingglass")
                            .foregroundColor(.gray)
                        TextField("Ïòà: ÌÉÄÏù¥Î†àÎÜÄ", text: $searchText)
                            .autocapitalization(.none)
                            .disableAutocorrection(true)

                        if !searchText.isEmpty {
                            Button(action: { searchText = "" }) {
                                Image(systemName: "xmark.circle.fill")
                                    .foregroundColor(.gray)
                            }
                        }
                    }
                    .padding(12)
                    .background(RoundedRectangle(cornerRadius: 10).fill(Color(.systemGray6)))
                    .overlay(RoundedRectangle(cornerRadius: 10).stroke(Color.gray.opacity(0.4), lineWidth: 1))

                    // MARK: Í≤ÄÏÉâ Î≤ÑÌäº
                    NavigationLink(destination: MedicationDetailView(medName: searchText)) {
                        Text("ÏïΩ ÏÑ±Î∂Ñ Î∂ÑÏÑùÌïòÍ∏∞")
                            .font(.headline)
                            .foregroundColor(.white)
                            .frame(maxWidth: .infinity)
                            .padding()
                            .background(Color.blue)
                            .cornerRadius(12)
                    }
                    .simultaneousGesture(TapGesture().onEnded {
                        saveSearchLog(searchText)
                    })
                    .disabled(searchText.isEmpty)

                    // MARK: ÏµúÍ∑º Í≤ÄÏÉâ
                    Text("ÏµúÍ∑º ÎÇ¥Í∞Ä Í≤ÄÏÉâÌïú ÏïΩ")
                        .font(.title3)
                        .bold()

                    if recentMeds.isEmpty {
                        Text("ÏµúÍ∑º Í≤ÄÏÉâÌïú ÏïΩÏù¥ ÏóÜÏñ¥Ïöî!")
                            .font(.subheadline)
                            .foregroundColor(.gray)
                    } else {
                        ScrollView(showsIndicators: true) {
                            VStack(spacing: 8) {
                                ForEach(recentMeds, id: \.self) { med in
                                    HStack {
                                        NavigationLink(destination: MedicationDetailView(medName: med)) {
                                            Text(med)
                                                .foregroundColor(.primary)
                                                .font(.system(size: 16, weight: .medium))
                                        }
                                        Spacer()
                                        Button(action: {
                                            deleteRecentMed(med)
                                        }) {
                                            Image(systemName: "xmark.circle.fill")
                                                .foregroundColor(.gray)
                                        }
                                    }
                                    .padding()
                                    .background(Color(.systemGray6))
                                    .cornerRadius(8)
                                }
                            }
                        }
                        .frame(maxHeight: 160)
                    }

                    // MARK: ÎßéÏù¥ Í≤ÄÏÉâÎêú ÏïΩ Top 5
                    Text("ÎßéÏù¥ Í≤ÄÏÉâÎêú ÏïΩ Top 5")
                        .font(.title3)
                        .bold()

                    VStack(spacing: 12) {
                        let paddedMeds = popularMeds + Array(repeating: "", count: max(0, 5 - popularMeds.count))
                        ForEach(Array(paddedMeds.prefix(5).enumerated()), id: \.offset) { index, med in

                            HStack(spacing: 12) {
                                CapsulePillBadge(
                                    text: rankText(for: index),
                                    colorLeft: rankColorLeft(for: index),
                                    colorRight: rankColorRight(for: index)
                                )

                                Text(med.isEmpty ? "‚Äî" : med)
                                    .font(.system(size: 18, weight: .medium))
                                    .foregroundColor(med.isEmpty ? .gray : .primary)

                                Spacer()

                                if !med.isEmpty {
                                    NavigationLink(destination: MedicationDetailView(medName: med)) {
                                        Image(systemName: "chevron.right")
                                            .foregroundColor(.gray)
                                    }
                                }
                            }
                            .padding()
                            .background(Color(.systemGray6))
                            .cornerRadius(12)
                        }
                    }
                }
                .padding()
            }
            .navigationTitle("ÌÖçÏä§Ìä∏Î°ú Í≤ÄÏÉâ")
            .onAppear {
                fetchRecentMeds()
                fetchPopularMeds()
            }
        }
    }

    // MARK: Îû≠ÌÇπ Í¥ÄÎ†®
    func rankText(for index: Int) -> String {
        switch index {
        case 0: return "ü•á 1ÏúÑ"
        case 1: return "ü•à 2ÏúÑ"
        case 2: return "ü•â 3ÏúÑ"
        case 3: return "4ÏúÑ"
        case 4: return "5ÏúÑ"
        default: return ""
        }
    }

    func rankColorLeft(for index: Int) -> Color {
        switch index {
        case 0: return Color.orange
        case 1: return Color.gray
        case 2: return Color.brown
        case 3: return Color.gray.opacity(0.3)   // ‚Üê Í∏∞Ï°¥ Ìù∞ÏÉâ ‚Üí Î∞ùÏùÄ ÌöåÏÉâ
        case 4: return Color.teal.opacity(0.3)   // ‚Üê Í∏∞Ï°¥ Ìù∞ÏÉâ ‚Üí Ïó∞Ìïú Ï≤≠Î°ùÏÉâ
        default: return Color.primary
        }
    }


    func rankColorRight(for index: Int) -> Color {
        switch index {
        case 0: return Color.orange.opacity(0.7)
        case 1: return Color.gray.opacity(0.7)
        case 2: return Color.brown.opacity(0.7)
        case 3: return Color.blue
        case 4: return Color.cyan
        default: return Color.primary
        }
    }

    // MARK: Ï†ÄÏû•
    func saveSearchLog(_ medName: String) {
        let db = Firestore.firestore()
        let timestamp = Timestamp(date: Date())

        db.collection("searchLogs").addDocument(data: [
            "medName": medName,
            "timestamp": timestamp
        ])

        db.collection("recentSearches")
            .document(userId)
            .collection("logs")
            .addDocument(data: [
                "medName": medName,
                "timestamp": timestamp
            ]) { error in
                if let error = error {
                    print("‚ùå ÏµúÍ∑º Í≤ÄÏÉâ Í∏∞Î°ù Ï†ÄÏû• Ïã§Ìå®: \(error)")
                } else {
                    print("‚úÖ ÏµúÍ∑º Í≤ÄÏÉâ Í∏∞Î°ù Ï†ÄÏû• ÏÑ±Í≥µ: \(medName)")
                    fetchRecentMeds()
                }
            }

        let popularRef = db.collection("popularMeds").document(medName)

        popularRef.getDocument { snapshot, error in
            if let document = snapshot, document.exists {
                let currentCount = document.data()?["count"] as? Int ?? 0
                popularRef.updateData(["count": currentCount + 1])
            } else {
                popularRef.setData(["medName": medName, "count": 1])
            }
        }
    }

    // MARK: ÏµúÍ∑º Í≤ÄÏÉâÏñ¥
    func fetchRecentMeds() {
        let db = Firestore.firestore()

        db.collection("recentSearches")
            .document(userId)
            .collection("logs")
            .order(by: "timestamp", descending: true)
            .getDocuments { snapshot, error in
                if let error = error {
                    print("‚ùå ÏµúÍ∑º Í≤ÄÏÉâÏñ¥ Î∂àÎü¨Ïò§Í∏∞ Ïã§Ìå®: \(error)")
                    return
                }

                let allMeds = snapshot?.documents.compactMap {
                    $0["medName"] as? String
                } ?? []

                var uniqueMeds: [String] = []
                var seen = Set<String>()
                for med in allMeds {
                    if !seen.contains(med) {
                        uniqueMeds.append(med)
                        seen.insert(med)
                    }
                }

                recentMeds = Array(uniqueMeds.prefix(10))
            }
    }

    // MARK: Ïù∏Í∏∞ ÏïΩ
    func fetchPopularMeds() {
        let db = Firestore.firestore()

        db.collection("popularMeds")
            .order(by: "count", descending: true)
            .limit(to: 5)
            .getDocuments { snapshot, error in
                if let error = error {
                    print("‚ùå Ïù∏Í∏∞ ÏïΩ Î∂àÎü¨Ïò§Í∏∞ Ïã§Ìå®: \(error)")
                    return
                }

                let sorted = snapshot?.documents.compactMap { doc in
                    doc.documentID  // ‚úÖ ÏïΩ Ïù¥Î¶Ñ = Î¨∏ÏÑú ID
                } ?? []

                popularMeds = Array(sorted.prefix(5))
            }
    }


    // MARK: ÏÇ≠Ï†ú
    func deleteRecentMed(_ medName: String) {
        let db = Firestore.firestore()

        db.collection("recentSearches")
            .document(userId)
            .collection("logs")
            .whereField("medName", isEqualTo: medName)
            .getDocuments { snapshot, error in
                if let error = error {
                    print("‚ùå ÏÇ≠Ï†ú Ïã§Ìå®: \(error)")
                    return
                }

                guard let docs = snapshot?.documents, !docs.isEmpty else {
                    print("‚ùóÔ∏èÏÇ≠Ï†úÌï† Î¨∏ÏÑú ÏóÜÏùå")
                    return
                }

                let batch = db.batch()
                for doc in docs {
                    batch.deleteDocument(doc.reference)
                }

                batch.commit { error in
                    if let error = error {
                        print("‚ùå ÏùºÍ¥Ñ ÏÇ≠Ï†ú Ïã§Ìå®: \(error)")
                    } else {
                        print("‚úÖ \(medName) ÏùºÍ¥Ñ ÏÇ≠Ï†ú ÏôÑÎ£å")
                        fetchRecentMeds()
                    }
                }
            }
    }
}
